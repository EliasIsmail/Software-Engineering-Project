
public class Activity {
	private int startWeek;
	private int endWeek;
	
	public Activity (int startWeek) {
		this.startWeek = startWeek;
	}
	public void setEndWeek(int endWeek) {
		this.endWeek = endWeek;
	}
	public int getStartWeek() {
		return startWeek;
	}
	public int getEndWeek() {
		return endWeek;
	}
	
	
}
